const int N = 2e3 + 5;
vector<int> adj[N];

signed main() {
  ios::sync_with_stdio(false); 
  cin.tie(0);
  int tt;
  cin >> tt;
  while(tt--) {
    int n, m;
    cin >> n >> m;
    for(int i = 1; i <= n; i++) {
      adj[i].clear();
    }
    for(int i = 0; i < m; i++) {
      int u, v;
      cin >> u >> v;
      adj[u].push_back(v);
      adj[v].push_back(u);
    }
    
  }
}



