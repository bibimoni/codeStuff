signed main() {
  ios::sync_with_stdio(false); 
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    int n, k;
    cin >> n >> k;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
      cin >> a[i];
    }
    sort(a.begin(), a.end(), greater<int>());
    int cur = 0;
    for (int i = 0; i < n; i++) {
      if (cur + a[i] > k) {
        break;
      }
      cur += a[i];
    }
    cout << k - cur << '\n';
  }
}


