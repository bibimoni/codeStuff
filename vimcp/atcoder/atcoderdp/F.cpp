signed main() {
    FAST;
    string s, t; cin >> s >> t;
    int n = s.size(), m = t.size();
    int dp[n + 1][m + 1];
    for(int i = 0; i <= n; i++) {
        for(int j = 0; j <= m; j++) {
            if(!i || !j) dp[i][j] = 0;
            else if(s[i - 1] == t[j - 1]) dp[i][j] = dp[i - 1][j - 1] + 1;
            else dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
        }
    }
    string res = "";
    while(n && m) {
        if(s[n - 1] == t[m - 1]) {
            res += s[n - 1];
            n--; m--;
        }
        else if(dp[n - 1][m] > dp[n][m - 1]) n--;
        else m--;
    }
    reverse(res.begin(), res.end());
    cout << res;
}

